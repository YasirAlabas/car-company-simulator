    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        model = (DefaultTableModel) jTable1.getModel();
        String serial = model.getValueAt(index, 0).toString();
        String brand = model.getValueAt(index, 1).toString();
        String plate = model.getValueAt(index, 2).toString();
        String color = model.getValueAt(index, 3).toString();
        String size = model.getValueAt(index, 4).toString();
        String model1 = model.getValueAt(index, 5).toString();
        String cost = model.getValueAt(index, 6).toString();
        Car car = CarSearch.search(Main.carList, Integer.parseInt(serial), brand, plate, color, size, model1, null, null, 0, 0).get(0);
        this.setVisible(false);
        new CostumerReservation(user, car).setVisible(true);


    }//GEN-LAST:event_jTable1MouseClicked
