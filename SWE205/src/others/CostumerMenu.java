    public void addRowToJtable() {
        model = (DefaultTableModel) jTable1.getModel();
        carList = CarSearch.search(Main.carList, 0, "", "", "", "", "", true, false, 0, 0);
        Object rowData[] = new Object[7];
        model.setRowCount(0);
        for (int i = 0; i < carList.size(); i++) {
            rowData[0] = carList.get(i).getSerialNum();
            rowData[1] = carList.get(i).getBrand();
            rowData[2] = carList.get(i).getPlateNum();
            rowData[3] = carList.get(i).getColor();
            rowData[4] = carList.get(i).getSize();
            rowData[5] = carList.get(i).getModel();
            rowData[6] = carList.get(i).getCostPerDay();
            model.addRow(rowData);

        }
    }

